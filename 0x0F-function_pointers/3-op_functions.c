#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - Function that adds two integers
 * @a: First operand
 * @b: Second operand
 * Return: Always The sum of two numbers if success
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Function that subtracts two integers
 * @a: First operand
 * @b: Second operand
 * Return: The difference of two numbers if success
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Fuction that returns the product of two numbers
 * @a: First operand
 * @b: Second operand
 * Return: The product of two numbers if success
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Fuction that returns the division of two numbers
 * @a: First operand
 * @b: Second operand
 * Return: The division of two numbers if success
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Fuction that returns the modulo of two numbers
 * @a: First operand
 * @b: Second operand
 * Return: The modulo of two numbers if success
 */
int op_mod(int a, int b)
{
	return (a % b);
}


